import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const frontendDir = path.resolve(scriptDir, "../..");
const errorsDir = path.resolve(frontendDir, "../i18n/errors");
const outputPath = path.resolve(
  frontendDir,
  "src/i18n/locales/error-codes.ts",
);
const checkOnly = process.argv.includes("--check");
const locales = ["zh-CN", "en-US"];

const sourceFiles = fs
  .readdirSync(errorsDir)
  .filter((file) => file.endsWith(".json"))
  .sort();
const catalog = new Map();

for (const sourceFile of sourceFiles) {
  const entries = JSON.parse(
    fs.readFileSync(path.join(errorsDir, sourceFile), "utf8"),
  );

  for (const [code, messages] of Object.entries(entries)) {
    if (catalog.has(code)) {
      throw new Error(
        `Duplicate error code ${code} in ${catalog.get(code).sourceFile} and ${sourceFile}`,
      );
    }
    for (const locale of locales) {
      if (typeof messages?.[locale] !== "string" || !messages[locale].trim()) {
        throw new Error(`${sourceFile}: ${code} is missing ${locale}`);
      }
    }
    catalog.set(code, { sourceFile, messages });
  }
}

const sortedEntries = [...catalog.entries()].sort(([left], [right]) =>
  left.localeCompare(right, "en"),
);

function renderMessages(locale) {
  return sortedEntries
    .map(
      ([code, { messages }]) =>
        `  ${JSON.stringify(code)}: ${JSON.stringify(messages[locale])},`,
    )
    .join("\n");
}

const output = `// Generated by scripts/i18n/sync-error-codes.mjs from i18n/errors/*.json.
// Do not edit this file directly.
export const zhCNErrorMessages = {
${renderMessages("zh-CN")}
} as const;

export const enUSErrorMessages = {
${renderMessages("en-US")}
} as const;
`;

if (checkOnly) {
  const current = fs.existsSync(outputPath)
    ? fs.readFileSync(outputPath, "utf8")
    : "";
  if (current !== output) {
    console.error(
      "Error-code translations are stale. Run `pnpm gen:error-codes` from frontend/.",
    );
    process.exit(1);
  }
  console.log(`Error-code translations are current (${catalog.size} codes).`);
} else {
  fs.writeFileSync(outputPath, output);
  console.log(`Generated ${catalog.size} error-code translations.`);
}
